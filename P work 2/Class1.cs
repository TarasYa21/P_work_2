using System;

namespace P_work_2
{
    public class Class1
    {
        public int PlusMathArea(int a, int b)
        {
            return a + b;
        }
        public int DeductionMathArea(int a, int b)
        {
            return a - b;
        }
    }
    public class Class2 {
        public int MultiplicationMathArea(int a, int b)
        {
            return a * b;
        }
        public double DivisionMathArea(double a, double b)
        {
            return a / b;
        }
    }
}
